                        <p><em><strong>“La Misión de la Iglesia Bautista de Viveros del Valle, tenía la capacidad y gozaba de su aprobación para constituirse a partir de ese momento en Iglesia, la que desde ése entonces llevaría el nombre de Iglesia Cristiana Bautista Resurrección”</strong></em></p>
