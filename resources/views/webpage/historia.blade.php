                    <p><em><strong>Amanecía una vez más, y Dios con su infinito Amor, mandó una Luz especial para que en un lugar de la Tierra, naciera una Misión Bautista, la cual tres años después, se convertiría en una Iglesia, cuyo nombre sería “Resurrección”.</strong></em></p>
                    <p>           Ésta, no sólo es una iglesia más, no es sólo un edificio cuyos fríos muros resguardan vanidades personales ni imperfecciones mundanas, pero es algo construido sobre la Roca Firme que es Cristo Jesús, dedicado y consagrado a Dios, lugar donde existe un púlpito del cual emanan sus mensajes para todos aquellos que se acercan a él.</p>
                    <p>            Aquí se inserta el principio, sin saber como será el final de ésta Iglesia de Dios, aunque por nuestra Fe creemos firmemente que Cristo Jesús volverá a reinar entre nosotros cuando sea el tiempo, pero con seguridad que utilizará también éste sagrado lugar dedicado a Él, para Reinar en éste mundo.<br />
