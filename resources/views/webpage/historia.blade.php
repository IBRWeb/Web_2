                    <p>            En la amplia zona constituida por Santa Mónica, Jacarandas, Atizapán, Las Arboledas y Las Margaritas, en el Estado de México, no había Obra Cristiana de la denominación Bautista, razón por la que varios hermanos tomaron la decisión de reunirse para constituir una misión Bautista, que sirviera para el desarrollo de la obra de nuestro Dios en esta importante zona. El día 24 de octubre de 1971, se reunieron alrededor de 25 hermanos procedentes de diversas iglesias bautistas, para iniciar una misión, la cual tomó el nombre de Misión Cristiana Bautista en Santa Mónica. Pasado el tiempo, los hermanos dedicaron la mayor parte de los meses siguientes a la constitución como misión, a la Oración y al estudio de la palabra de Dios. Puesto que no éramos misión de ninguna iglesia constituida sino que, por así decirlo, fue espontánea, funcionaron como iglesia, celebrando la Cena del Señor (de acuerdo con la Biblia) que era impartida siempre por un Pastor Bautista ordenado, tenían sesiones de negocios regulares y comisiones que funcionaban bien. </p>
                    <p>            Sin embargo, los hermanos estaban convencidos de que si seguían actuando de esta manera, terminarían aislándose. En consecuencia de esto, decidieron pedir a una iglesia constituida que los acogiera como misión suya. Después de orar con fervor, la elección de los hermanos fue la Iglesia Bautista de Viveros del Valle, en la cual presentaron la petición y que tomó su tiempo para conocer bien a la iglesia. Meses después, dieron su aprobación y nos acogieron como misión suya.<br /></p>
                    <p>            A los pocos meses de la constitución, se buscó un terreno para la construcción de nuestro templo, el cual nuestro Dios nos lo dio, en la avenida principal de Santa Mónica. Pagando el enganche que se necesitaba para adquirir la propiedad, se siguieron congregando en la casa de uno de los hermanos, pero como ya no cabían en ella, pidieron a nuestro Dios nuevamente su ayuda e iniciaron la construcción de nuestra iglesia, la cual al tener acabadas las paredes y poner una lona como techo, empezaron a congregarse.<br /></p>
                    <p>            El día 15 de diciembre se reunió el Concilio de Iglesias Bautistas que decidiría sobre la constitución de nuestra misión como iglesia. El Concilio estaba constituido por las siguientes iglesias, las cuales interrogaron ampliamente y examinaron la misión:</p>
